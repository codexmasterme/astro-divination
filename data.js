// 组装小阿尔卡纳为标准牌对象（结构与大阿尔卡纳一致，便于统一渲染）
function buildMinor() {
  const cards = [];
  for (const suit of MINOR_SUITS) {
    for (let r = 1; r <= 14; r++) {
      const meta = RANK_META[r];
      const m = MINOR_MEANINGS[suit.key][r];
      cards.push({
        id: suit.key + r,
        arcana: 'minor',
        roman: meta.badge,                       // 角标（A/2../侍骑后王）
        name: suit.name + meta.cn,               // 如 权杖首牌 / 圣杯国王
        en: meta.en + ' of ' + suit.en,
        symbol: suit.symbol,
        astro: suit.element + '象 · ' + suit.name,
        keywords: m.kw,
        up: m.up,
        rev: m.rev,
        advice: m.adv,
      });
    }
  }
  return cards;
}

/* 构建完整 78 张塔罗牌堆 */
function buildDeck() {
  return MAJOR.concat(buildMinor());
}

