// 从自由文本里匹配城市（取名字最长的匹配，兼容「浙江杭州」「杭州市」等写法）
function geocodeCity(placeStr) {
  if (!placeStr) return null;
  const s = placeStr.replace(/\s/g, '');
  let best = null;
  for (const c of CITIES) {
    if (s.indexOf(c.name) !== -1) {
      if (!best || c.name.length > best.name.length) best = c;
    }
  }
  return best;
}

